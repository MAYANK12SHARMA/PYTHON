# #?    Function in string          
#!                                        topic :- changing the case of a string:

#? 1. lower(): Converts all characters in a string to lowercase
#! SYNTAX :- string_name.lower()


str1 = 'HELLO WORLD'
# Convert the string to lowercase
lowercase_str = str1.lower()
print(lowercase_str)

print('\n__________________________________________________________________________________\n')

#? 1. upper(): Converts all characters in a string to uppercase
#! SYNTAX :- string_name.upper()


str1 = 'hello world'
# Convert the string to uppercase
uppercase_str = str1.upper()
print(uppercase_str)

print('\n__________________________________________________________________________________\n')

#? 1. capitalize(): Converts the first character of a string to uppercase and the rest to lowercase.
#! SYNTAX :- string_name.capitalize()


str1 = 'HELLO WORLD'
# Convert the string to capitalize
capitalize_str = str1.capitalize()
print(capitalize_str) 
   
print('\n__________________________________________________________________________________\n')

#? 1. swapcase(): Converts all characters in a string to swapcase
#! SYNTAX :- string_name.swapcase()


str1 = 'HEllO WOrLd'
# Convert the string to swapcase
swapcase_str = str1.swapcase()
print(swapcase_str) 
print('\n__________________________________________________________________________________\n')

#? 1. swapcase(): Swaps the case of each character in a string (i.e., converts uppercase to lowercase and vice versa).
#! SYNTAX :- string_name.swapcase()


str1 = 'HEllO WOrLd'
# Convert the string to swapcase
swapcase_str = str1.swapcase()
print(swapcase_str) 

print('\n__________________________________________________________________________________\n')

#? 1. title(): Converts the first character of each word in a string to uppercase and the rest to lowercase.
#! SYNTAX :- string_name.title()


str1 = 'HEllO WOrLd'
# Convert the string to title
title_str = str1.title()
print(title_str) 

