# #?                                LIST FUNCTION
# !     Topic:- index()

#? The index() method in Python is used to find the index of the first occurrence of a specified element in a list. 
# ! SYNTAX :-value =  list.index(element, start, end)
#? element: The element for which the index is to be found.
#? start (optional): The starting index for the search.
#? end (optional): The ending index for the search.


fruits = ['apple', 'banana', 'orange', 'banana', 'grape']
index_of_banana = fruits.index('banana')

print(index_of_banana)

