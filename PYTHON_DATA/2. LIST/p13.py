# #?                                LIST FUNCTION
# !     Topic:- pop()

#? The pop() method is used to remove and return an element from a specific index in a list

# ! SYNTAX :- element = list.pop(index)
# ? index: The index of the element to be removed. If not specified, the last element is removed


fruits = ['apple', 'banana', 'orange', 'grape']

removed_fruit = fruits.pop(1)

print(removed_fruit)

print(fruits)


