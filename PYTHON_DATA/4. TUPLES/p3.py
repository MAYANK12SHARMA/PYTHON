# #?                           Topic :- Operations on tuples 
#?      len() function

#?  The len() function is used to determine the length (number of elements) of a sequence, and it can be applied to tuples as well.

#! SYNTAX :- len(tuple_name)

# Define a tuple
tup = (10, 20, 30, 40, 50,'mayank','')

# Use the len() function to get the length of the tuple
tuple_length = len(tup)

# Print the original tuple and its length

print("Tuple:", tup)
print("Length of the tuple:", tuple_length)
