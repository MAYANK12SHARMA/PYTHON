# Define a tuple 'tup' with elements 1, 2, 3, 4, and 5
tup = (1, 2, 3, 4, 5)

# Multiply the tuple 'tup' by 3 to create a new tuple 'tup1' with repeated elements
tup1 = tup * 3

# Print the result to observe the repeated elements in the tuple 'tup1'
print(tup1)
